(function () {
  'use strict';

  // Функція для відключення вікового рейтингу всередині карток
  function disableAgeRating() {
    // Перевірка на наявність елементів з віковим рейтингом на картках і їх приховування
    $('.card__age-rating').each(function () {
      $(this).hide(); // Приховуємо віковий рейтинг
    });
    console.log('[DisableAgeRating] Віковий рейтинг вимкнено всередині карток');
  }

  // Викликаємо функцію для вимкнення вікового рейтингу
  disableAgeRating();

  // Додаємо спостерігач за новими картками, щоб вимкнути віковий рейтинг при їх завантаженні
  setInterval(function() {
    disableAgeRating(); // Перевіряємо і знову вимикаємо віковий рейтинг
  }, 2000); // Перевіряємо кожні 2 секунди, щоб нові картки також оброблялися

})();